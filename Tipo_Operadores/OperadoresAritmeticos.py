produto_1 = 10
produto_2 = 20
#                                             Ordem de Precedêcia
print("Potênciação =", produto_1 ** produto_2)      #1°
print("Divisão Interira =", produto_1 // produto_2) #2°
print("Multiplicação =", produto_1 * produto_2)     #3°
print("Divisão", produto_1 / produto_2)             #3°
print("Resto =", produto_1 % produto_2)             #4°
print("Soma =", produto_1 + produto_2)              #5°      
print("Subtração =", produto_1 - produto_2)         #5°